    /**
     * Handles the HTTP <code>GET</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doGet(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        String sort=request.getParameter("sort");
        String order;
        if("popular".equals(sort)) {                         //a-z and popular
            order="count(RecordID) desc";          
        } 
        else 
            order ="category asc";
        String xpage = request.getParameter("page");
        int page,size, limit=20, start, end;               //paging
        if(xpage==null) page=1;
        else page=Integer.parseInt(xpage);
        BookDAO b =new BookDAO();
        List<String> list= b.allCategories("order by "+order);      
        size=list.size();
        int numpage=size/limit+(size%limit==0?0:1);
        start=(page-1)*limit;
        if(page*limit>size){
            end=size;
        }
        else{
            end=page*limit;
        }
        request.setAttribute("page", page);
        request.setAttribute("numpage", numpage);
        request.setAttribute("categories", b.getCategoryByPage(list, start, end));
        request.getRequestDispatcher("Categories.jsp").forward(request, response);
    }

